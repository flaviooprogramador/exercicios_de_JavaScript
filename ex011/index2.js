let num1 = window.prompt('Primeiro valor')
let num2 = window.prompt('Segundo valor')

res0 = num1 + num2
res1 = num1 - num2
res2 = num1 * num2
res3 = num1  / num2
window.prompt(`A soma é ${num1} + ${num2} = ${res0}`)
window.prompt(`A subtração é ${num1} - ${num2} = ${res1}`)
window.prompt(`A soma é ${num1} * ${num2} = ${res2}`)
window.prompt(`A soma é ${num1} / ${num2} = ${res3}`)